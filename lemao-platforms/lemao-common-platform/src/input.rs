pub enum InputEvent {
    Unknown,
    WindowMoved(i32, i32),
    WindowSizeChanged(u32, u32),
    WindowClosed,
    KeyPressed(Key),
    KeyReleased(Key),
    CharPressed(char),
    MouseButtonPressed(MouseButton),
    MouseButtonReleased(MouseButton),
    MouseMoved(i32, i32),
    MouseWheelRotated(i32),
}

